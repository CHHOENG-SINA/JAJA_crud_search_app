    private void btnremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveActionPerformed
       int index = table_data.getSelectedRow();
       if(index<0){
           JOptionPane.showMessageDialog(null, "Please Select Record to Remove!");
           return;
       }
       int click = JOptionPane.showConfirmDialog(null, "Do you want to Delete?","Delete",JOptionPane.YES_NO_OPTION);
       if(click==JOptionPane.NO_OPTION){return;}
       String cusId = table_data.getValueAt(index, 0).toString();
       try{
       String sqlremove = "Delete From tblcustomer where cusId =" +cusId;
       Statement sm = DBManager.getConnection().createStatement();
       st.executeUpdate(sqlremove);
       loadDataToJTable("SELECT * FROM tblcustomer");
       }catch(Exception ex){
           JOptionPane.showMessageDialog(null, ex);
       }
    }//GEN-LAST:event_btnremoveActionPerformed

