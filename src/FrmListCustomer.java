     public void loadDataToJTable(String sql){
        try{
            rs = st.executeQuery(sql);
            String header[] = {"CusId","Name","Gender","Phone","Address"};
            int rowindex = 0;
            int row = 0;
            int col = 5;
            rs.last();
            row = rs.getRow();
            String data[][] = new String[row][col];
            rs.beforeFirst();
            while(rs.next()){
                data[rowindex][0] = rs.getString(1);
                data[rowindex][1] = rs.getString(2).trim();
                data[rowindex][2] = rs.getString(3).trim();
                data[rowindex][3] = rs.getString(4);
                data[rowindex][4] = rs.getString(5);
                rowindex +=1;
            }
            table_data.setModel(new DefaultTableModel(data,header));
            // Set Single Section 
            table_data.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            table_data.setColumnSelectionAllowed(false);
            rs.close();
            int count_female = 0;
            for(int i =0;i<table_data.getRowCount();i++){
                if(table_data.getValueAt(i, 2).toString().trim().equals("F")){
                    count_female++;
                }
            }
            totalnum.setText(Integer.toString(table_data.getRowCount()));
            totalfemale.setText(Integer.toString(count_female));
        }catch (Exception ex){
            System.out.println(ex.toString());
        }
    }

